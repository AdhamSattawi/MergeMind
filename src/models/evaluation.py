"""
MergeMind — Agent Evaluation Models

Pydantic V2 definitions for the structured output returned by Gemini.
Forces the LLM to return scores and reasoning in a machine-readable format.
"""

from typing import List, Optional
from pydantic import BaseModel, Field


class EvaluationMetrics(BaseModel):
    """The 4-dimensional scoring matrix evaluated by the AI."""
    
    logic_and_efficiency: int = Field(
        ...,
        ge=0,
        le=100,
        description="Algorithmic correctness, time/space complexity, and optimization.",
    )
    architectural_soundness: int = Field(
        ...,
        ge=0,
        le=100,
        description="Code modularity, adherence to SOLID principles, clean decoupling.",
    )
    robustness_and_security: int = Field(
        ...,
        ge=0,
        le=100,
        description="Exception handling, edge-case resilience, and security posture.",
    )
    test_coverage_contribution: int = Field(
        ...,
        ge=0,
        le=100,
        description="Quality, relevance, and presence of added tests.",
    )


class CodeEvaluation(BaseModel):
    """The complete evaluation report generated by the Arbitration Agent."""
    
    impact_score: float = Field(
        ...,
        ge=0,
        le=100,
        description="The overall weighted impact score of the code contribution.",
    )
    metrics: EvaluationMetrics
    summary_verdict: str = Field(
        ...,
        description="A 2-3 sentence objective summary explaining the evaluation.",
    )
    critical_bottlenecks: List[str] = Field(
        default_factory=list,
        description="List of critical issues or vulnerabilities found, if any.",
    )
    refactoring_suggestions: List[str] = Field(
        default_factory=list,
        description="Constructive suggestions for improving the code.",
    )
    is_relevant: bool = Field(
        default=True,
        description="True if the code directly solves the problem described in the linked issue.",
    )
    is_suspicious: bool = Field(
        default=False,
        description="True if the code appears to be AI-generated bloat or an attempt to game the system.",
    )
    suspicion_reason: Optional[str] = Field(
        default=None,
        description="Explanation of why the contribution was flagged as suspicious.",
    )
